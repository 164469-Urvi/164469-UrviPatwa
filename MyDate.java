package DataDifferenceProvider;

public class MyDate {
private int dD;
private int mm;
private int yyyy;

MyDate(int dd,int mm,int yyyy)
{
	this.dD=dd;
	this.mm=mm;
	this.yyyy=yyyy;
	
}
public int getdD()
{
	return dD;
	
}
public int setdD()
{
	return dD;
	
}
public int setmm()
{
	return mm;
	
}
public int getmm()
{
	return mm;
	
}
public int setyyyy()
{
	return yyyy;
	
}
public int getyyyy()
{
	return yyyy;
	
}
}